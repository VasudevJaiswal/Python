# Escape Sequence Characters:?
# Sequence of characters after backslash ‘\’ [Escape Sequence Characters]

# Escape Sequence Characters comprises of more than one character but represents one character when used within the string.

# Examples: \n (new line), \t (tab), \’ (single quote), \\ (backslash), etc.

#Escape sequence for New Line : \n

exp = "Vasudev Jaiswal is good. \n He is Founder of Jaiswa's Development Studio"
print(exp)

#Escape Sequence for Tab line :  \t 

exp = "Vasudev Jaiswal is good. \t He is Founder of Jaiswa's Development Studio"
print(exp)

#Escape Sequence for Single quote : \

exp = "Vasudev Jaiswal is good. \ He is Founder of Jaiswa's Development Studio"
print(exp)

#Escape Sequence for Backslash 
exp = "Vasudev Jaiswal is good. \\  He is Founder of Jaiswa's Development Studio"
print(exp)